#!/bin/bash

# Variables and data types in Bash
# There are no data types in Bash
# In Bash, a variable is capable of storing numeric values, individual characters, or strings of characters

# Assign the value directly
my_name=Morteza

# Assign the value based on existing variable value
# $ is required to access an existing variable's value
new_name=$my_name

echo $my_name $new_name
